// Build/prep-time converter (research D3, FR-FND-014 / task T039).
//
// Reads the governed workbook `item_bank` worksheet and emits a committed, typed module
// `src/mocks/governed-bank.ts`, preserving every source field exactly. It NEVER fabricates
// fields absent from the source (e.g. `weight`, `difficulty`) — constitution VI.
//
// The emitted module is imported ONLY via a dynamic import() in questionBankService, so the
// bundler code-splits the full 543-item bank out of the initial chunk (SC-005).
//
// Usage:
//   npm i -D xlsx           # one-time: the converter's only (dev-only) dependency
//   npm run convert:bank    # node scripts/convert-item-bank.mjs
//
// Source workbook (default): ../project/uploads/nexus_governed_master_questionnaire_bank_final.xlsx
// Override with: BANK_XLSX=/path/to/file.xlsx npm run convert:bank
//
// NOTE: until this is run against the real workbook, src/mocks/governed-bank.ts holds a
// REPRESENTATIVE subset (all five method families incl. blocked/quarantine) so the app and
// the provenance test run without the binary asset present.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const FRONTEND = resolve(__dirname, '..');
const REPO = resolve(FRONTEND, '..');
const XLSX_PATH =
  process.env.BANK_XLSX ||
  resolve(REPO, 'project/uploads/nexus_governed_master_questionnaire_bank_final.xlsx');
const OUT = resolve(FRONTEND, 'src/mocks/governed-bank.ts');
const SHEET = 'item_bank';

// Source column header -> ItemBankItem field. Only fields present in the model are mapped;
// any extra source columns are ignored (never invented as data).
const COLUMN_MAP = {
  item_id: 'itemId',
  domain_id: 'domainId',
  domain_name: 'domainName',
  dimension_id: 'dimensionId',
  dimension_name: 'dimensionName',
  facet_id: 'facetId',
  facet_name: 'facetName',
  method_family: 'methodFamily',
  item_format: 'itemFormat',
  item_text: 'itemText',
  keyed_answer: 'keyedAnswer',
  response_scale: 'responseScale',
  bank_state: 'bankState',
  use_status: 'useStatus',
  review_status: 'reviewStatus',
  job_level_overlay: 'jobLevelOverlay',
  reverse_scored: 'reverseScored',
};

async function main() {
  let XLSX;
  try {
    XLSX = (await import('xlsx')).default ?? (await import('xlsx'));
  } catch {
    console.error(
      "[convert:bank] Missing dev dependency 'xlsx'. Run: npm i -D xlsx, then npm run convert:bank.",
    );
    process.exit(1);
  }

  const buf = readFileSync(XLSX_PATH);
  const wb = XLSX.read(buf, { type: 'buffer' });
  const ws = wb.Sheets[SHEET];
  if (!ws) {
    console.error(`[convert:bank] Worksheet "${SHEET}" not found in ${XLSX_PATH}.`);
    process.exit(1);
  }
  const rows = XLSX.utils.sheet_to_json(ws, { defval: null });

  const items = [];
  for (const row of rows) {
    const it = {};
    for (const [col, field] of Object.entries(COLUMN_MAP)) {
      if (row[col] === undefined) continue;
      it[field] = row[col];
    }
    if (!it.itemId) continue; // skip corrupt/partial rows rather than fabricate (spec edge case)
    it.reverseScored =
      it.reverseScored === true || String(it.reverseScored).toLowerCase() === 'true';
    it.options = parseOptions(row);
    it.keyedAnswer = it.keyedAnswer ?? null;
    items.push(it);
  }

  const banner =
    '// AUTO-GENERATED by scripts/convert-item-bank.mjs — DO NOT EDIT BY HAND.\n' +
    '// Governed item bank (code-split — imported only by questionBankService; kept out of the initial chunk).\n' +
    "import type { ItemBankItem } from '@/models';\n\n";
  const body =
    'export const governedBank: readonly ItemBankItem[] = ' +
    JSON.stringify(items, null, 2) +
    ';\n';
  writeFileSync(OUT, banner + body, 'utf8');
  console.log(`[convert:bank] Wrote ${items.length} items to ${OUT}.`);
}

function parseOptions(row) {
  const opts = {};
  for (const key of ['a', 'b', 'c', 'd', 'e']) {
    const v = row[`option_${key}`];
    if (v != null && String(v).trim() !== '') opts[key] = String(v);
  }
  return opts;
}

main().catch((err) => {
  console.error('[convert:bank] Failed:', err);
  process.exit(1);
});
